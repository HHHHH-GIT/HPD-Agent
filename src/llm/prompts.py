    "【示例】\n"
    "任务:“将'Hello World'转换为小写” → easy\n"
    "任务:“计算 12345 * 67890” → easy(单步算术,模型可靠)\n"
    "任务:“解释量子纠缠并给出三个应用” → hard(需要知识验证,且易幻觉)\n"
    "任务:“从文本中提取所有日期,并按时间排序” → hard(多步+排序逻辑易错)\n\n"
    "子任务信息:\n"
    "任务ID:{task_id}\n"
    "任务名称:{task_name}\n"
