    "2. **需要多步推导且中间结果易错**:即使模型能写步骤,但某一步容易引入符号错误、边界遗漏或逻辑跳跃(例如数学证明、算法设计、复杂条件分支)。\n"
    "3. **输出格式有严格的⾮结构化约束**:例如必须输出特定JSON schema,且字段之间存在依赖或计算关系,单次生成容易漏字段或算错。\n"
    "4. **存在常见的“幻觉陷阱”**:问题涉及数字比较、时间顺序、否定词嵌套、或反常识结论,模型容易翻车。\n\n"
    "【以下情况判 easy】\n"
    "- 简单的信息提取、格式转换、同义改写、单步计算(如加减乘除)。\n"
    "- 问题明确、答案唯一、不需要额外上下文补全。\n"
    "- 即使模型犯错,也能被后续简单规则校验出来(例如枚举值匹配)。\n\n"
